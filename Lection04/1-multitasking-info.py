# Введение в многозадачность в Python
#
# В современном программировании часто возникает необходимость
#   выполнять несколько задач одновременно.
# Для этого используется многозадачность —
#   способность компьютера или программы обрабатывать несколько задач одновременно.
#
# В Python существует несколько подходов к реализации многозадачности,
# каждый из которых имеет свои преимущества и недостатки.
# Прежде чем начать использовать многозадачность в своих программах,
# необходимо понимать основные понятия и подходы.
#
# Поток выполнения —
#   это независимая последовательность инструкций,
#   которая может выполняться параллельно с другими потоками.
#   Каждый поток имеет свой стек вызовов и свой контекст выполнения.
#
# Процесс —
#   это экземпляр программы, который запущен на компьютере.
#   Каждый процесс имеет своё собственное адресное пространство,
#   в котором хранятся данные и код программы.
#   Процессы могут выполняться параллельно на разных ядрах процессора.
